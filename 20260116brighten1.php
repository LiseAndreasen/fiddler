$max = 500;

for($r=1;$r<=$max;$r++) {
	for($h=$r;$h<=$max;$h++) {
		// R = r * 3 * h / âˆš(4h2 - r2)
		$R = $r * 3 * $h / sqrt(4 * $h * $h - $r * $r);
		if(floor($R) == $R) {
			printf("HIT! r: %3d, h: %3d, R: %8.4f\n", $r, $h, $R);			
		}
	}
}

